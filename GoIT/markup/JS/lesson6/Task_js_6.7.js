function unique(value, index, arr) {
    var t = [];
    for (var k in arr)
        t[k] = arr[k].toLowerCase().split("").sort().join("");
    var edited = value.toLowerCase().split("").sort().join("");

    return t.indexOf(edited) == index;
}

function anClean(arr){
    return arr.filter(unique);
}

